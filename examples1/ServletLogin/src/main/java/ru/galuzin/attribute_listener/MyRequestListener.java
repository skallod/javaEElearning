package ru.galuzin.attribute_listener;

import javax.servlet.ServletContext;
import javax.servlet.ServletRequest;
import javax.servlet.ServletRequestEvent;
import javax.servlet.ServletRequestListener;
import javax.servlet.http.HttpServletRequest;

/**
 * Created by User on 31.12.2016.
 */
public class MyRequestListener implements ServletRequestListener {
    // ??????? ????? ????????? ? ????????
    private static int reqCount;

    public void requestInitialized(ServletRequestEvent e) {
        //????? ??????????? ??? ??????? ? log-?????
        ServletContext context = e.getServletContext();
        System.out.println("request initialized thread name "+Thread.currentThread().getName());
        //????? ??????????? ??? ????????? ?????????? ? ???????
        ServletRequest req = e.getServletRequest();
        synchronized (context) {
            String name = "";
            name = ((HttpServletRequest) req).getRequestURI();
            // ?????????? ???????? ???????? ? log-????
            context.log("Request for " + name + "; Count=" + ++reqCount);
        }
    }
    public void requestDestroyed(ServletRequestEvent e) {
        // ?????????? ??? ??????????? ???????
        System.out.println("Request ?????????");
        System.out.println("request destroyed thread name "+Thread.currentThread().getName());

    }
}
